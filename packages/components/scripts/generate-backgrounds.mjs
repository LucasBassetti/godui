// Generates the four PatternCraft background components from the vendored
// `gridPatterns` data. Re-run with `pnpm --filter @godui/components generate:backgrounds`
// after refreshing scripts/vendor/patterncraft-patterns.ts.
//
// Source: https://github.com/megh-bari/pattern-craft (src/data/patterns.ts)
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const vendorPath = path.join(__dirname, "vendor", "patterncraft-patterns.ts");
const srcDir = path.join(__dirname, "..", "src");

// One component per PatternCraft category. Names are fixed JS identifiers.
const CATEGORIES = {
  gradients: { component: "GradientBackground", dir: "gradient-background" },
  geometric: { component: "GeometricBackground", dir: "geometric-background" },
  decorative: {
    component: "DecorativeBackground",
    dir: "decorative-background",
  },
  effects: { component: "EffectBackground", dir: "effect-background" },
};

// Load gridPatterns without a TS toolchain: strip the type import + the array
// type annotation, then evaluate. The data uses no `${}` interpolation, so the
// `code` template literals are inert under eval (verified before committing).
function loadPatterns() {
  let src = fs.readFileSync(vendorPath, "utf8");
  src = src.replace(/import\s+\{[^}]*\}\s+from\s+["'][^"']*["'];?/g, "");
  src = src.replace(
    /export\s+const\s+gridPatterns\s*:\s*Pattern\[\]\s*=/,
    "const gridPatterns =",
  );
  // eslint-disable-next-line no-new-func
  return new Function(`${src}\nreturn gridPatterns;`)();
}

function toKebab(id) {
  return String(id).trim();
}

function styleLiteral(style) {
  const entries = Object.entries(style).map(
    ([k, v]) => `    ${JSON.stringify(k)}: ${JSON.stringify(v)},`,
  );
  return `{\n${entries.join("\n")}\n  }`;
}

function componentSource({ component, patterns }) {
  const variantType = `${component}Variant`;
  const propsType = `${component}Props`;
  const variantsConst = `${component.charAt(0).toLowerCase()}${component.slice(1)}Variants`;
  const ids = patterns.map((p) => toKebab(p.id));
  const list = ids.map((id) => `  ${JSON.stringify(id)},`).join("\n");
  const records = patterns
    .map((p) => `  ${JSON.stringify(toKebab(p.id))}: ${styleLiteral(p.style)},`)
    .join("\n");
  const defaultVariant = JSON.stringify(ids[0]);

  return `// AUTO-GENERATED by scripts/generate-backgrounds.mjs — do not edit by hand.
// Ported from PatternCraft (https://github.com/megh-bari/pattern-craft).
import * as React from "react";

/** Every ${component} pattern id, in source order. */
export const ${variantsConst} = [
${list}
] as const;

export type ${variantType} = (typeof ${variantsConst})[number];

const variantStyles: Record<${variantType}, React.CSSProperties> = {
${records}
};

export type ${propsType} = React.HTMLAttributes<HTMLDivElement> & {
  /** Background pattern to render. */
  variant?: ${variantType};
};

/**
 * Full-bleed decorative background. Drop it as the first child of a \`relative\`
 * container; your content sits above it at \`z-raised\` or higher.
 */
const ${component} = React.forwardRef<HTMLDivElement, ${propsType}>(
  ({ variant = ${defaultVariant}, className, style, ...props }, ref) => (
    <div
      ref={ref}
      data-slot=${JSON.stringify(CATEGORIES_BY_COMPONENT[component].dir)}
      aria-hidden="true"
      className={\`absolute inset-0 z-base \${className ?? ""}\`}
      style={{ ...variantStyles[variant], ...style }}
      {...props}
    />
  ),
);
${component}.displayName = ${JSON.stringify(component)};

export { ${component} };
`;
}

const CATEGORIES_BY_COMPONENT = Object.fromEntries(
  Object.values(CATEGORIES).map((c) => [c.component, c]),
);

function indexSource({ component }) {
  const dir = CATEGORIES_BY_COMPONENT[component].dir;
  const variantsConst = `${component.charAt(0).toLowerCase()}${component.slice(1)}Variants`;
  return `export {\n  ${component},\n  type ${component}Props,\n  type ${component}Variant,\n  ${variantsConst},\n} from "./${dir}";\n`;
}

function main() {
  const patterns = loadPatterns();
  const byCategory = new Map();
  for (const p of patterns) {
    if (!byCategory.has(p.category)) byCategory.set(p.category, []);
    byCategory.get(p.category).push(p);
  }

  const summary = [];
  for (const [category, meta] of Object.entries(CATEGORIES)) {
    const group = byCategory.get(category) ?? [];
    if (group.length === 0) {
      console.warn(
        `! category "${category}" has no patterns — skipping ${meta.component}`,
      );
      continue;
    }
    const dir = path.join(srcDir, meta.dir);
    fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(
      path.join(dir, `${meta.dir}.tsx`),
      componentSource({ component: meta.component, patterns: group }),
    );
    fs.writeFileSync(
      path.join(dir, "index.ts"),
      indexSource({ component: meta.component }),
    );
    summary.push(`${meta.component}: ${group.length} variants`);
  }

  console.log(`Generated:\n  ${summary.join("\n  ")}`);
}

main();
